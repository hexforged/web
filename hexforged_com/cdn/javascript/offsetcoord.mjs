import { Hex } from './hex.min.mjs';

/**
 * Class representing an offset coordinate in an odd-r layout.
 */
class OffsetCoord {
  /**
   * Creates an instance of OffsetCoord.
   * @param {number} col - The column in the offset coordinate system.
   * @param {number} row - The row in the offset coordinate system.
   */
  constructor(col, row) {
    this.col = col;
    this.row = row;
  }
  to_cube() {
    const q = this.col - (this.row + (this.row & 1)) / 2;
    const r = this.row;
    return new Hex(q, r, -q-r);
  }
}

export { OffsetCoord };
export default OffsetCoord;